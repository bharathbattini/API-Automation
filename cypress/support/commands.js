Cypress.Commands.add('GetApis', (getMethods, getUrl, accessToken, getUsercode) => {

    return cy.api({
        method: getMethods,
        url: getUrl,
        failOnStatusCode: false,
        headers: {

            authorization: `Bearer ${accessToken}`

        },
        qs: {

            user_code: getUsercode
        }

    });

});

